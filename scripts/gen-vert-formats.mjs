#!/usr/bin/env node
/**
 * Extract format definitions from VERT reference repo and emit vert-formats-data.js.
 * Source: https://github.com/VERT-sh/VERT.git
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const vertRoot = process.env.VERT_REPO || '/tmp/VERT'

function parseFormatInfos(source, label) {
  const re = /new FormatInfo\("([^"]+)",\s*(true|false),\s*(true|false)(?:,\s*(true|false))?\)/g
  const formats = []
  let m
  while ((m = re.exec(source)) !== null) {
    formats.push({
      name: m[1],
      fromSupported: m[2] === 'true',
      toSupported: m[3] === 'true',
      isNative: m[4] === undefined ? true : m[4] === 'true',
    })
  }
  if (!formats.length) throw new Error(`No formats parsed for ${label}`)
  return formats
}

function readVert(relPath) {
  return readFileSync(join(vertRoot, relPath), 'utf8')
}

const magickManual = parseFormatInfos(readVert('src/lib/converters/magick.svelte.ts'), 'magick manual')
const magickAuto = parseFormatInfos(readVert('src/lib/converters/magick-automated.ts'), 'magick automated')
const ffmpeg = parseFormatInfos(readVert('src/lib/converters/ffmpeg.svelte.ts'), 'ffmpeg')
const vertd = parseFormatInfos(readVert('src/lib/converters/vertd.svelte.ts'), 'vertd')
const pandoc = parseFormatInfos(readVert('src/lib/converters/pandoc.svelte.ts'), 'pandoc')

// Deduplicate image formats (manual overrides automated)
const imageMap = new Map()
for (const f of [...magickAuto, ...magickManual]) {
  imageMap.set(f.name, f)
}
const imageFormats = [...imageMap.values()]

const out = `// AUTO-GENERATED by scripts/gen-vert-formats.mjs — do not edit by hand
// Source: https://github.com/VERT-sh/VERT.git

/** @typedef {{ name: string, fromSupported: boolean, toSupported: boolean, isNative: boolean }} VertFormat */

/** @type {VertFormat[]} */
export const IMAGE_FORMATS = ${JSON.stringify(imageFormats, null, 2)}

/** @type {VertFormat[]} */
export const FFMPEG_FORMATS = ${JSON.stringify(ffmpeg, null, 2)}

/** @type {VertFormat[]} */
export const VIDEO_FORMATS = ${JSON.stringify(vertd, null, 2)}

/** @type {VertFormat[]} */
export const DOCUMENT_FORMATS = ${JSON.stringify(pandoc, null, 2)}

export const CONVERTER_ENGINES = {
  imagemagick: 'imagemagick',
  ffmpeg: 'ffmpeg',
  pandoc: 'pandoc',
}
`

const dest = join(root, 'src/tools/vert-formats-data.js')
writeFileSync(dest, out)
console.log(`gen-vert-formats: ${imageFormats.length} image, ${ffmpeg.length} ffmpeg, ${vertd.length} video, ${pandoc.length} doc formats → ${dest}`)
